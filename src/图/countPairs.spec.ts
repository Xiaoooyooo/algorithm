import assert from "assert";
import countPairs from "./countPairs";

describe("countPairs", () => {
  it("go", () => {
    assert.deepEqual(
      countPairs(
        2,
        [
          [1, 2],
          [2, 1]
        ],
        [1, 2]
      ),
      [1, 0]
    );
    assert.deepEqual(
      countPairs(
        4,
        [
          [1, 2],
          [2, 4],
          [1, 3],
          [2, 3],
          [2, 1]
        ],
        [2, 3]
      ),
      [6, 5]
    );
    assert.deepEqual(
      countPairs(
        5,
        [
          [1, 5],
          [1, 5],
          [3, 4],
          [2, 5],
          [1, 3],
          [5, 1],
          [2, 3],
          [2, 5]
        ],
        [1, 2, 3, 4, 5]
      ),
      [10, 10, 9, 8, 6]
    );
    assert.deepEqual(
      countPairs(
        6,
        [
          [5, 2],
          [3, 5],
          [4, 5],
          [1, 5],
          [1, 4],
          [3, 5],
          [2, 6],
          [6, 4],
          [5, 6],
          [4, 6],
          [6, 2],
          [2, 6],
          [5, 4],
          [6, 1],
          [6, 1],
          [2, 5],
          [1, 3],
          [1, 3],
          [4, 5]
        ],
        [6, 9, 2, 1, 2, 3, 6, 6, 6, 5, 9, 7, 0, 4, 5, 9, 1, 18, 8, 9]
      ),
      [
        15, 13, 15, 15, 15, 15, 15, 15, 15, 15, 13, 15, 15, 15, 15, 13, 15, 0,
        14, 13
      ]
    );
  });
});
